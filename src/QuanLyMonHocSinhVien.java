            PreparedStatement pst = con.prepareStatement(insertData);  
            pst.setString(1, ID);
            pst.setString(2, MaSV);
            pst.setString(3, TenSV);
            pst.setString(4, MaMon);
            pst.setString(5, TenMon);
            pst.setString(6, HocKy);
            pst.setString(7, Diem);
            pst.setString(8,TrangThai );
            pst.setString(9, MaGV);

            pst.executeUpdate();
            getTable();
