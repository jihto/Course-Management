        String TenSV = jComboBox2.getSelectedItem().toString();
        String MaMon = jComboBox3.getSelectedItem().toString();
        String TenMon = jComboBox4.getSelectedItem().toString();
        String Diem = jTextField6.getText();
        String HocKy = jComboBox5.getSelectedItem().toString();
        String MaGV = jComboBox6.getSelectedItem().toString(); 
