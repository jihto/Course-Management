    public boolean checkExist(String Id){
           DBAccess ac = new DBAccess();
           if(Id != ""){
              String query = "select * from Tbl_MonHoc where ID = '" + Id + "'";
               try {
                   ResultSet rs =  ac.Query(query);
                   if(rs.next()){
                       return true;
                   }
               } catch (Exception e) {
                   JOptionPane.showMessageDialog(null, "Something Error");
               }
           } 
           return false; 
       }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         String ID = jTextField1.getText();  
        try {
                if(checkExist(ID)){
                    DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                    model.setRowCount(0);
                    
                    String updateData = "UPDATE Tbl_QuanLyMonHocSinhVien SET MaSV =  ?, TenSV = ?, MaMon = ?, TenMon = ?, HocKy = ?, Diem = ?, TrangThai = ?, MaGV = ? WHERE ID = '" + ID + "'"; 
                    MyConnection connect = new MyConnection();
                    Connection con =  connect.getConnection();
                    PreparedStatement pst = con.prepareStatement(updateData);  
                    
                    pst.setString(1, jComboBox1.getSelectedItem().toString());
                    pst.setString(2, jComboBox2.getSelectedItem().toString());
                    pst.setString(3, jComboBox3.getSelectedItem().toString());
                    pst.setString(4, jComboBox4.getSelectedItem().toString());
                    
                    
                    pst.setString(5, jComboBox5.getSelectedItem().toString());
                    
                    pst.setString(6, jTextField6.getText());
                    
                    pst.setString(8, jComboBox6.getSelectedItem().toString()); 
                    if(jRadioButton1.isSelected()){
                       pst.setString(7, "Dau"); 
                    }else{
                        pst.setString(7, "Truot"); 
                    }
                    pst.execute();  
                    getTable();
                }  
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } 
    }//GEN-LAST:event_jButton2ActionPerformed

