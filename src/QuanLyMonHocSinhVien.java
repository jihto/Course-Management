    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here: 
         JTable source = (JTable)evt.getSource();
            int row = source.rowAtPoint( evt.getPoint() ); 
            jTextField1.setText(source.getModel().getValueAt(row,0).toString());  
            jComboBox1.setSelectedItem(source.getModel().getValueAt(row,1));  
            jComboBox2.setSelectedItem(source.getModel().getValueAt(row,2));  
            jComboBox3.setSelectedItem(source.getModel().getValueAt(row,3));  
            
             
            jComboBox4.setSelectedItem(source.getModel().getValueAt(row,4)); 
            jTextField6.setText(source.getModel().getValueAt(row,5).toString()); 
            
            jComboBox5.setSelectedItem(source.getModel().getValueAt(row,6));   
            jComboBox6.setSelectedItem(source.getModel().getValueAt(row,7));  
            if(source.getModel().getValueAt(row,8).toString().compareTo("Dau") == 7){
                jRadioButton1.setSelected(true);
            }else{
                jRadioButton2.setSelected(true);
            }
            
            jTextField1.setEnabled(false);
    }//GEN-LAST:event_jTable1MouseClicked

