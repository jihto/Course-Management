    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String MaSV = jTextField1.getText();
        String TenSV = jTextField2.getText();
        String SDT = jTextField3.getText();
        String QueQuan = jTextField4.getText();
        String GioiTinh;
        if(jRadioButton1.isSelected()){
            GioiTinh = "nam";
        }
        else{
            GioiTinh = "nu";
        }
        try {
            DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
            model.setRowCount(0);
            MyConnection connect = new MyConnection();
            Connection con =  connect.getConnection();
            String insertData = "insert into Tbl_SinhVien values(?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(insertData);  
            pst.setString(1, MaSV);
            pst.setString(2, TenSV);
            pst.setString(3, SDT);
            pst.setString(4, QueQuan);
            pst.setString(5, GioiTinh);

            pst.executeUpdate();
            getTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String ID = jTextField1.getText();  
        try {
                if(checkExist(ID)){
                    DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
                    model.setRowCount(0);
                    
                    String updateData = "UPDATE Tbl_SinhVien SET TenSV =  ?, SDT = ?, QueQuan = ?, GioiTinh = ?   WHERE MaSV = '" + ID + "'"; 
                    MyConnection connect = new MyConnection();
                    Connection con =  connect.getConnection();
                    PreparedStatement pst = con.prepareStatement(updateData);  
                    pst.setString(1, jTextField2.getText());
                    pst.setString(2, jTextField3.getText());
                    pst.setString(3, jTextField4.getText()); 
                    if(jRadioButton1.isSelected()){
                       pst.setString(4, "nam"); 
                    }else{
                        pst.setString(4, "nu"); 
                    }
                    pst.execute();  
                    getTable();
                }  
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Update Fail");
        } 
    }//GEN-LAST:event_jButton2ActionPerformed

