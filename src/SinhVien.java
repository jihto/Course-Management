    public boolean checkExist(String Id){
        DBAccess ac = new DBAccess();
        if(Id != ""){
           String query = "select * from Tbl_MonHoc where ID = '" + Id + "'";
            try {
                ResultSet rs =  ac.Query(query);
                if(rs.next()){
                    return true;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Something Error");
            }
        } 
        return false; 
    }
