    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here: 
            JTable source = (JTable)evt.getSource();
            int row = source.rowAtPoint( evt.getPoint() ); 
            jTextField1.setText(source.getModel().getValueAt(row,0).toString());
            jTextField2.setText(source.getModel().getValueAt(row,1).toString());
            jTextField3.setText(source.getModel().getValueAt(row,2).toString());
            jTextField4.setText(source.getModel().getValueAt(row,3).toString());  
            String GioiTinh = source.getModel().getValueAt(row,4).toString();   
            if(GioiTinh.compareTo("nam") == 7){
                jRadioButton1.setSelected(true);
            }else { 
                jRadioButton2.setSelected(true); 
            }
            jTextField1.setEnabled(false);
    }//GEN-LAST:event_jTable2MouseClicked

