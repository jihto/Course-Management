    public void getTable(){
        try {
            DBAccess ac = new DBAccess();
            ResultSet rs =  ac.Query("select * from Tbl_MonHoc");
            while(rs.next()){
                String id = String.valueOf(rs.getInt("ID"));
                String MaMon = rs.getString("MaMon");
                String TenMon = rs.getString("TenMon");
                String HocKy = rs.getString("HocKy");
                String table[] = {id, MaMon, TenMon, HocKy};
                DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                model.addRow(table);
            }
        } catch (Exception ex) { 
            JOptionPane.showMessageDialog(null, ex.toString(),"Loi",JOptionPane.ERROR_MESSAGE);
        }
    }
