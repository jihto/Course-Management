    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ID = jTextField1.getText();
        String MaMon = jTextField2.getText();
        String TenMon = jTextField3.getText();
        String HocKy = jTextField4.getText();
        try {
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            MyConnection connect = new MyConnection();
            Connection con =  connect.getConnection();
            String insertData = "insert into Tbl_MonHoc values(?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(insertData);  
            pst.setInt(1, Integer.parseInt(ID));
            pst.setString(2, MaMon);
            pst.setString(3, TenMon);
            pst.setString(4, HocKy);
            pst.executeUpdate();
            getTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

